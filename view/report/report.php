<h3>Hur känns det att hoppa rakt in i klasser med PHP, gick det bra?</h3>
<p>Det gick ganska trögt att komma in i det (vilket märks på min sena inlämnling...) men efter
att ha läst artiklarna och gjort övningarna x-antal gånger känns det som jag har bättre koll. Jag
trodde faktiskt jag skulle klara av det bättre, då jag arbetat med klasser i C++ tidigare där man
alltid använder sig av kontruktorer samt "getters och setters". Det visade sig att jag hade glömt bort
så gott som allt jag gjort tidigare men det är ju ändå runt två år sedan jag satt med C++.</p>
<p>Samma sak med GET, POST och SESSION som vi använde i htmlphp-kursen; jag trodde jag skulle komma ihåg
mer än jag faktiskt gjorde, så det var bara att sätta sig ned och läsa igenom den "gamla" artikeln
om grunderna i PHP. Sedan kollade jag mycket på videos för att verkligen få allt att fastna. Att endast
läsa verkar inte riktigt vara rätt inlärningsmetod för mig men videos är det som verkligen hjälper mig.
Så när allt material inte fanns på dbwebb fick jag leta runt på YouTube istället och det hjälpte
väldigt mycket så jag hoppas det ska gå fortare med nästkommande kursmoment.</p>

<h3>Berätta om dina reflektioner kring ramverk, anax-lite och din me-sida.</h3>
<p>Det var kul att hoppa in i ramverk igen och den här gången få bygga upp ett själv. Det här med
hemsidor och webbdesign är ju något jag verkligen brinner för så detta moment gick mycket fortare att
lösa och jag är redan väldigt nöjd med min sida. Finslip har jag inte riktigt hunnit med så det kommer
fixas under kursens gång. Jag ser fram emot att fortsätta bygga upp sidan och se till att den blir så
snygg och användarvänlig som möjligt!</p>
<p>Jag ser väldigt mycket likheter med anax-flat som vi tidigare jobbat med men jag valde att använda
"vanlig" CSS istället för LESS i detta ramverk. Det skulle vara kul att bli ett riktigt proffs på det
så jag kände att jag ville designa min hemsida helt i CSS och se hur det går. Hittills fungerar det
alldeles utmärkt och sidan ser minst lika bra ut som om jag hade använt mig av LESS.</p>

<h3>Gick det bra att komma igång med MySQL, har du liknande erfarenheter sedan tidigare?</h3>
<p>Jag gjorde inte hela uppgiften (vilket inte heller var tanken) men det verkar just nu vara
väldigt likt SQLite som vi arbetade med i htmlphp-kursen. Så det är väl det jag har som tidigare
erfarenhet: SQLite.</p>

