<h1>Välkommen till min sida i Anax-lite!</h1>
<p><b>Jag heter Viktoria "Viza" Haapaoja och jag läser programmet Webbprogrammering på Blekinge Tekniska Högskola.</b></p>

<p>Jag kommer ursprungligen från den lilla orten/hålan Strömsholm som ligger ett par mil utanför Västerås. Hjärtat tillhör
dock Karlskrona, där jag varit bosatt sedan sensommaren 2014.</p>

<p>Mitt liv fylls av programmering, kåraktivitet och gym om man ska hålla det kort. Jag har varit aktiv i kåren sedan min
första vecka på BTH och läsåret 15/16 satt jag med i kårhusets styrelse som ledamot.</p>

<p>Mina kunskaper inom programmering leder tillbaka till hösten 2014 då jag gick programmet Civilingenjör i spel- och
programvaruteknik. Det innehöll en hel del C-programmering (C och C++) men efter ca 1,5 år på utbildningen kände
jag att det inte var det jag ville göra, så jag sökte till Webbprogrammering och det passar mig mycket bättre! Här
har jag lärt mig grunderna i Python3, HTML, PHP, CSS, JavaScript (Vanilla och Node JS) och Bash.</p>

<p>Under våren har jag även hoppat in i världen bestående av objektorientering i både Python och PHP. Jag har dock
använt objektorientering och klasser i C++ förr, i mitt gamla program, men det är alltid trevligt att lära sig det
i mer webbrelaterade progam. Sedan är det skönt att fräscha upp minnet lite igen.</p>

<p>Några saker jag gillar att göra på den lilla fritid jag har är att fotografera och måla i akvarell. Det har gått
lite segt på den fronten senaste tiden men efter att ha införskaffat ett nytt teleobjektiv till kameran hoppas jag på
fler fototillfällen under våren. Jag har lite tankar om att gå med i denna fotogrupp som Mos har skapat...</p>
